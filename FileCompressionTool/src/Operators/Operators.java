// Java operators are special symbols that perform operations ob variables or values
// These operators are key in programming as they help in data manipulation.
// Example of a code that describes the basic structure of an operator

class Operators {
    public static void main(String[] args){
        // Declare and initiaization of variables
        int num1 = 500;
        int num2 = 1000;

        // using the addition operator
        int sum = num1 + num2;
        System.out.println("The sum is: " +sum);

        // Using the subraction operator
        int diff = num2- num1;
        System.out.println("The difference is: " +diff);

    }


}