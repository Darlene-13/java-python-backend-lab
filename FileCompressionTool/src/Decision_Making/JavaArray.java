



public class JavaArray{
    public static void main(String[] args){
        // Initializing an array
        int [] arr = {40,55,63,17,22};

        // Size of array
        int n = arr.length;

        // traversing an array
        for (int i=0; i < n; i++){
            System.out.println(arr[i] + " ");
        }
    }
}

