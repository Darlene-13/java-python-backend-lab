class ImplicitExplicitConstructors{

    // Default constructor that is created when the object is created.
    ImplicitExplicitConstructors(){
        System.out.println("ImplicitExplicitConstructors");
    }

    // Driver function
    public static void main(String[] args){
        ImplicitExplicitConstructors obj = new ImplicitExplicitConstructors();
    }
}

// Output = ImplicitExplicitConstructors


