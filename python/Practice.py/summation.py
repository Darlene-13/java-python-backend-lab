# Enter two random numbers to get their addition subtraction, multiplication and division
num1 = int(input("Enter the first number: "))
num2 = int(input("Enter the second number: "))
output1 = num1 + num2
output2 = num1 - num2
output3 = num1 * num2
output4 = num1 / num2
print(f"The summation is: {output1}")
print(f"The difference is: {output2}")
print(f"The product is: {output3}")
print(f"The division is: {output4}")

